float ENiXStatsAverage(float *Vector) {
  int                                   C,Size=Vector[0]+1;
  float                                 Av=0;
  for(C=1;C<Size;C++)                   Av+=Vector[C];
                                        Av/=(--Size);
  return                                Av;                               }

float ENiXStatsVariance(float *Vector){
  int                                   C,Size=Vector[0]+1;
  float                                 R,Mean=ENiXStatsAverage(Vector);
  for(C=1;C<Size;C++)                   R+=(Vector[C]*Vector[C]);
                                        R/=--(Size); R-=(Mean*Mean);
  return                                R;                                }
