#include "ENiX_Maths.h"

int ENiXFactorial(int n){
  int R=1,C;
  n++;
  for(C=1;C<n;C++)
    R*=C;
  return R;
}

int ENiXPower(int Base, int Exp){
  int C,R=1;
  for(C=0;C<Exp;C++)
    R*=Base;
  return R;
}

